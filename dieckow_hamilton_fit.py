def build_obs_matrix():
    """
    Load taxonomy TSVs в†’ obs[patient] = np.array (5, 3)
    Column order: [So, An, Vd, Fn, Pg], rows: [week1, week2, week3] transposed.
    Returns: obs dict[patient_id в†’ (5, 3) array]
    """
    manifest = pd.read_csv(MANIFEST, sep='\t')
    records = {}
    for _, row in manifest.iterrows():
        tsv = TAXONOMY_DIR / f"{row['sample']}_taxonomy.tsv"
        if not tsv.exists():
            continue
        df = pd.read_csv(tsv, sep='\t')
        total = df['count'].sum()
        counts = {g: c / total for g, c in zip(df['genus'], df['count'])}
        records[row['sample']] = counts

    missing = set(manifest['sample']) - set(records)
    if missing:
        print(f"WARNING: {len(missing)} samples not yet processed: {sorted(missing)}")

    obs = {}
    for _, row in manifest.iterrows():
        patient = row['patient']
        tp = int(row['timepoint']) - 1   # 0-indexed
        sample = row['sample']
        if sample not in records:
            continue
        if patient not in obs:
            obs[patient] = np.zeros((N_SP, 3))
        for k, genus in enumerate(GENERA_5):
            # sum all matching genus-level counts (substring match)
            val = sum(v for g, v in records[sample].items()
                      if genus.lower() in g.lower())
            obs[patient][k, tp] = val

    # Renormalize each timepoint
    for patient in obs:
        for t in range(3):
            s = obs[patient][:, t].sum()
            if s > 0:
                obs[patient][:, t] /= s

    return obs


def load_or_build_data(force=False):
    if not force and DATA_CACHE.exists():
        with open(DATA_CACHE) as f:
            d = json.load(f)
        obs = {p: np.array(v) for p, v in d['obs'].items()}
        print(f"Loaded cache: {len(obs)} patients, genera={GENERA_5}")
        return obs

    obs = build_obs_matrix()
    DATA_CACHE.parent.mkdir(parents=True, exist_ok=True)
    with open(DATA_CACHE, 'w') as f:
        json.dump({'genera': GENERA_5, 'obs': {p: v.tolist() for p, v in obs.items()}}, f, indent=2)
    print(f"Saved cache: {DATA_CACHE}")
    return obs


