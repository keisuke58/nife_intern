def extract_patient_theta(theta, patient_idx):
    """
    Joint theta (65) в†’ single-patient theta (20) for simulate_0d_nsp.
    Layout: [A_utri(15), b_patient(5)]
    """
    A_utri = theta[:N_A_PARAMS]
    b_start = N_A_PARAMS + patient_idx * N_SP
    b = theta[b_start: b_start + N_SP]
    return np.concatenate([A_utri, b])


def patient_index(patient_id):
    return PATIENT_ORDER.index(patient_id)


