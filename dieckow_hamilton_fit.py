MAX_PRED = 2   # max prediction weeks per patient (compile-time constant)


def simulate_patient(theta_20, phi_init, n_weeks=2):
    """NumPy wrapper for single-patient simulation (used by rmse_joint/info)."""
    if not HAS_JAX:
        raise RuntimeError("JAX not available")
    theta_jax = jnp.array(theta_20, dtype=jnp.float64)
    phi_cur = jnp.array(phi_init, dtype=jnp.float64)
    preds = []
    for _ in range(n_weeks):
        traj = simulate_0d_nsp(
            theta_jax, n_sp=N_SP, n_steps=N_STEPS, dt=DT,
            phi_init=phi_cur, c_const=C_CONST, alpha_const=ALPHA_CONST,
        )
        pb = np.array(traj[-1])
        s = pb.sum()
        pb = pb / s if s > 0 else pb
        preds.append(pb)
        phi_cur = jnp.array(pb)
    return np.array(preds)


def _sim_2weeks_jax(theta_20, phi_init):
    """Pure JAX: run 2 consecutive ODE weeks, return (2, N_SP) phibar."""
    theta_64 = theta_20.astype(jnp.float64)
    phi_cur = phi_init.astype(jnp.float64)
    # Week 1
    traj1 = simulate_0d_nsp(theta_64, n_sp=N_SP, n_steps=N_STEPS, dt=DT,
                              phi_init=phi_cur, c_const=C_CONST, alpha_const=ALPHA_CONST)
    phi1 = traj1[-1]
    phi1 = phi1 / jnp.maximum(phi1.sum(), 1e-12)
    # Week 2
    traj2 = simulate_0d_nsp(theta_64, n_sp=N_SP, n_steps=N_STEPS, dt=DT,
                              phi_init=phi1, c_const=C_CONST, alpha_const=ALPHA_CONST)
    phi2 = traj2[-1]
    phi2 = phi2 / jnp.maximum(phi2.sum(), 1e-12)
    return jnp.stack([phi1, phi2])   # (2, N_SP)


def _log_lik_one_patient(theta_20, phi_init, obs_future, valid, sigma):
    """JAX pure: log-likelihood for one patient.
    theta_20: (20,), phi_init: (N_SP,),
    obs_future: (MAX_PRED, N_SP), valid: (MAX_PRED,) 0/1 mask.
    """
    preds = _sim_2weeks_jax(theta_20, phi_init)          # (2, N_SP)
    residuals = preds - obs_future                        # (2, N_SP)
    ll_per_week = -0.5 * jnp.sum((residuals / sigma)**2, axis=1)  # (2,)
    return jnp.sum(ll_per_week * valid)


# vmapped over patient axis вЂ” called with batched arrays
_log_lik_patients_vmap = jax.vmap(
    _log_lik_one_patient, in_axes=(0, 0, 0, 0, None)
)


def make_log_likelihood_joint_jax(obs, sigma=0.05):
    """Build a pure-JAX log_likelihood(theta) closure over the patient batch.

    Pre-computes phi_init_batch, obs_future_batch, valid_batch as JAX arrays.
    Inner vmap evaluates all N_PATIENTS in parallel on GPU.
    """
    phi_init_list, obs_future_list, valid_list = [], [], []

    for patient in PATIENT_ORDER:
        if patient not in obs:
            phi_init_list.append(np.ones(N_SP, dtype=np.float64) / N_SP)
            obs_future_list.append(np.zeros((MAX_PRED, N_SP), dtype=np.float64))
            valid_list.append(np.zeros(MAX_PRED, dtype=np.float64))
            continue
        phi_obs = obs[patient]                        # (N_SP, 3)
        weeks_ok = int((phi_obs.sum(axis=0) > 0).sum())

        phi0 = phi_obs[:, 0].astype(np.float64)
        phi0 = np.clip(phi0, 1e-6, 1.0)
        phi0 /= phi0.sum()
        phi_init_list.append(phi0)

        obs_f = np.zeros((MAX_PRED, N_SP), dtype=np.float64)
        valid = np.zeros(MAX_PRED, dtype=np.float64)
        n_pred = min(weeks_ok - 1, MAX_PRED)
        for t in range(n_pred):
            obs_f[t] = phi_obs[:, t + 1]
            valid[t] = 1.0
        obs_future_list.append(obs_f)
        valid_list.append(valid)

    # Static JAX arrays (captured by closure)
    phi_init_b  = jnp.array(np.array(phi_init_list),   dtype=jnp.float64)  # (P, N_SP)
    obs_future_b = jnp.array(np.array(obs_future_list), dtype=jnp.float64)  # (P, 2, N_SP)
    valid_b     = jnp.array(np.array(valid_list),       dtype=jnp.float64)  # (P, 2)
    sigma_jax   = jnp.float64(sigma)

    def log_likelihood(theta):
        """theta: (65,) в†’ scalar log-likelihood (GPU vmap over 10 patients)."""
        theta64 = theta.astype(jnp.float64)
        A_utri  = theta64[:N_A_PARAMS]   # (15,)

        # Build (N_PATIENTS, 20) theta batch via vmap over patient indices
        def theta_for_patient(i):
            b_start = N_A_PARAMS + i * N_SP
            b_i = jax.lax.dynamic_slice(theta64, (b_start,), (N_SP,))
            return jnp.concatenate([A_utri, b_i])

        theta_batch = jax.vmap(theta_for_patient)(
            jnp.arange(N_PATIENTS, dtype=jnp.int32))      # (P, 20)

        ll_per_patient = _log_lik_patients_vmap(
            theta_batch, phi_init_b, obs_future_b, valid_b, sigma_jax)  # (P,)
        return jnp.sum(ll_per_patient)

    return log_likelihood


