def log_likelihood_joint(theta, obs, sigma=0.05):
    """
    Sum log-likelihood over all patients.
    obs: dict[patient_id в†’ (5, 3) array]
    """
    ll = 0.0
    for i, patient in enumerate(PATIENT_ORDER):
        if patient not in obs:
            continue
        phi_obs = obs[patient]
        weeks_ok = (phi_obs.sum(axis=0) > 0).sum()
        if weeks_ok < 2:
            continue
        theta_p = extract_patient_theta(theta, i)
        phi_init = phi_obs[:, 0]
        n_pred = weeks_ok - 1
        try:
            pred = simulate_patient(theta_p, phi_init, n_weeks=n_pred)
            obs_future = phi_obs[:, 1:1+n_pred].T
            residuals = pred - obs_future
            ll += -0.5 * np.sum((residuals / sigma) ** 2)
        except Exception:
            ll += -1e12
    return ll


def log_prior_joint(theta):
    """NumPy version вЂ” used only by legacy rmse_joint path."""
    A_utri = theta[:N_A_PARAMS]
    b_all  = theta[N_A_PARAMS:]
    if np.any(A_utri < -5) or np.any(A_utri > 5):
        return -np.inf
    if np.any(b_all < 0.01) or np.any(b_all > 15):
        return -np.inf
    lp = 0.0
    for k, (expected_sign, sigma) in _META_SIGN_PRIOR.items():
        v = A_utri[k]
        if expected_sign > 0 and v < 0:
            lp += -0.5 * (v / sigma) ** 2
        elif expected_sign < 0 and v > 0:
            lp += -0.5 * (v / sigma) ** 2
    return lp


def make_log_prior_jax():
    """Pure-JAX soft sign prior for tmcmc_nuts_engine (log_prior_fn arg)."""
    items = list(_META_SIGN_PRIOR.items())  # static at compile time

    def log_prior_fn(theta):
        A_utri = theta[:N_A_PARAMS].astype(jnp.float64)
        lp = jnp.float64(0.0)
        for k, (exp_sign, sig) in items:
            v = A_utri[k]
            if exp_sign > 0:
                lp = jnp.where(v < 0, lp - 0.5 * (v / sig) ** 2, lp)
            else:
                lp = jnp.where(v > 0, lp - 0.5 * (v / sig) ** 2, lp)
        return lp

    return log_prior_fn


def rmse_joint(theta, obs):
    total, n = 0.0, 0
    for i, patient in enumerate(PATIENT_ORDER):
        if patient not in obs:
            continue
        phi_obs = obs[patient]
        weeks_ok = (phi_obs.sum(axis=0) > 0).sum()
        if weeks_ok < 2:
            continue
        theta_p = extract_patient_theta(theta, i)
        phi_init = phi_obs[:, 0]
        n_pred = weeks_ok - 1
        pred = simulate_patient(theta_p, phi_init, n_weeks=n_pred)
        obs_future = phi_obs[:, 1:1+n_pred].T
        total += np.sum((pred - obs_future) ** 2)
        n += pred.size
    return float(np.sqrt(total / n)) if n > 0 else float('nan')


