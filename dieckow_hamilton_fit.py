def _make_prior_bounds():
    """Return [N_PARAMS, 2] prior bounds array for tmcmc_nuts_engine."""
    bounds = np.zeros((N_PARAMS, 2))
    bounds[:N_A_PARAMS]  = [-5.0, 5.0]    # A upper triangle
    bounds[N_A_PARAMS:]  = [0.01, 15.0]   # per-patient b
    return bounds


def run_tmcmc_joint(obs, n_particles=200, sigma_ll=0.05, output_path=None,
                    mutation='rw', use_sign_prior=False):
    """
    GPU-accelerated joint TMCMC (shared-A, per-patient-b, 65 params).
    Uses jax.vmap over 10 patients (inner) + vmap over particles (outer).
    """
    from tmcmc_nuts_engine import tmcmc_engine

    print(f"  n_sp={N_SP}, n_params={N_PARAMS} (A={N_A_PARAMS}, b={N_B_PARAMS})")
    print(f"  n_patients={N_PATIENTS}, n_particles={n_particles}, mutation={mutation}")
    print(f"  sign_prior={'ON' if use_sign_prior else 'OFF'}")

    log_likelihood = make_log_likelihood_joint_jax(obs, sigma=sigma_ll)
    bounds = _make_prior_bounds()
    log_prior_fn = make_log_prior_jax() if use_sign_prior else None

    # Warm-up JIT compile check
    import time
    t0 = time.time()
    _ = jax.jit(log_likelihood)(jnp.zeros(N_PARAMS, dtype=jnp.float32))
    print(f"  JIT compile: {time.time()-t0:.1f}s")

    result = tmcmc_engine(
        log_likelihood=log_likelihood,
        prior_bounds=bounds,
        mutation=mutation,
        n_particles=n_particles,
        log_prior_fn=log_prior_fn,
        label=f'Dieckow-5sp-joint-{mutation}',
        verbose=True,
    )

    map_theta = np.array(result['theta_MAP'])
    r = rmse_joint(map_theta, obs)
    print(f"  MAP RMSE: {r:.4f}")

    out = {
        'theta_map':    map_theta.tolist(),
        'samples':      np.array(result['samples']).tolist(),
        'rmse':         r,
        'n_stages':     result.get('n_stages', -1),
        'genera':       GENERA_5,
        'patient_order': PATIENT_ORDER,
        'n_A_params':   N_A_PARAMS,
        'n_B_params':   N_B_PARAMS,
        'sign_prior':   use_sign_prior,
    }
    if output_path:
        Path(output_path).parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w') as f:
            json.dump(out, f, indent=2)
        print(f"  Saved: {output_path}")
    return out


def _init_theta_from_heine():
    """Load Heine DH MAP theta as initial A; set b=1 for all patients."""
    RUNS = Path('/home/nishioka/IKM_Hiwi/Tmcmc202601/data_5species/_runs')
    path = RUNS / 'dh_baseline' / 'theta_MAP.json'
    theta0 = np.zeros(N_PARAMS)
    theta0[N_A_PARAMS:] = 1.0   # b = 1 default

    if path.exists():
        with open(path) as f:
            d = json.load(f)
        raw = np.array(d.get('theta_full', d.get('theta_sub')))
        # Extract upper triangle from Heine layout (same as attractor_analysis.py)
        A = np.zeros((5, 5))
        A[0,0]=raw[0]; A[0,1]=A[1,0]=raw[1]; A[1,1]=raw[2]
        A[2,2]=raw[5]; A[2,3]=A[3,2]=raw[6]; A[3,3]=raw[7]
        A[0,2]=A[2,0]=raw[10]; A[0,3]=A[3,0]=raw[11]
        A[1,2]=A[2,1]=raw[12]; A[1,3]=A[3,1]=raw[13]
        A[4,4]=raw[14]
        A[0,4]=A[4,0]=raw[16]; A[1,4]=A[4,1]=raw[17]
        A[2,4]=A[4,2]=raw[18]; A[3,4]=A[4,3]=raw[19]
        b_heine = np.array([raw[3], raw[4], raw[8], raw[9], raw[15]])
        k = 0
        for r in range(5):
            for c in range(r, 5):
                theta0[k] = A[r, c]; k += 1
        # Use Heine b as starting point for all patients
        for i in range(N_PATIENTS):
            theta0[N_A_PARAMS + i*N_SP: N_A_PARAMS + (i+1)*N_SP] = b_heine
        print("  Initialized A from Heine DH MAP theta")
    return theta0


